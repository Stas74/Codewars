/*
Combine strings function
Create a function named combineNames that accepts two parameters (first and last name).
 The function should return the full name.

Example:

combineNames('James', 'Stevens')
returns:

'James Stevens'
*/

// Create the combineNames function here
const combineNames = (name1, name2) => `${name1} ${name2}`;

console.log(combineNames("James", "Stevens")); // 'James Stevens'
